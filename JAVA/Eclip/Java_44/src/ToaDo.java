
public class ToaDo {
    private int x , y ;
    public ToaDo (int x, int y)
    {
        this.x = x ;
        this.y = y ;
        
    }
    // get  and set 
    public int getX()
    {
    	return this.x;
    }
    public void setX(int x)
    {
    	this.x = x ;
    }
    public int getY()
    {
    	return this.y;
    }
    public void setY(int y)
    {
    	 this.y= y;
    }
}
