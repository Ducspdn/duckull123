
public class Ngay {
    private int ngay ;
    private int thang ;
    private int nam ;
    // constructer
    public Ngay (int ngay , int thang , int nam)
    {
    	this.ngay = ngay ;
    	this.thang = thang ;
    	this.nam = nam ;
    }
    // get va set 
    public int getNgay()
    {
    	return this.ngay ;
    }
    public void setNgay(int ngay)
    {
    	this.ngay = ngay ;
    }
    public int getThang()
    {
    	return this.ngay ;
    }
    public void setThang(int thang)
    {
    	this.thang = thang ;
    }
    public int getNam()
    {
    	return this.nam ;
    }
    public void setNam(int nam)
    {
    	this.nam = nam ;
    }
}
